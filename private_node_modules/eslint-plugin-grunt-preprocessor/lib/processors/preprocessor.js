"use strict";

// import processors
module.exports.processors = {

    '.js': {
        preprocess: function(text, filename) {

            // Overwrite the grunt hooks before passing to ESLint
            // Some can be blank, but in other cases it's best to put an empty //, {}, [], etc.
            text = text.replace('<%= grunt.file.read(css_filename) %>', '');
            text = text.replace('<%= grunt.file.read(dependencies) %>', '{}');
            text = text.replace('<%= grunt.file.read(hbs_output) %>', '// eslint-disable-line lines-around-comment');
            text = text.replace('<%= grunt.file.read(tests) %>', '// eslint-disable-line lines-around-comment');
            text = text.replace("<%= grunt.option('tool') %>", '');
            
            return [text];
        },
        postprocess: function(messages, filename) {
            return messages[0];
        }
    },
};

